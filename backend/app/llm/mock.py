def _topic_for(persona: dict) -> str:
    """상대 페르소나의 아키타입 → 대화 주제. mock 외 페르소나는 내용 해시로 폴백."""
    cs = persona.get("communication_style", "") or "default"
    for a in _ARCHETYPES:
        if a["communication_style"] == cs:
            return a["topic"]
    topics = list(_DIALOGUES.keys())
    return topics[_stable_index(cs, len(topics))]


